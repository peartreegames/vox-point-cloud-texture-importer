namespace VoxPointCloudTextureImporter.Editor
{
    public class VoxPoint
    {
        public int x;
        public int y;
        public int z;
        public int i;

        public VoxPoint(int x, int y, int z, int i)
        {
            this.x = x;
            this.y = y;
            this.z = z;
            this.i = i;
        }
    }
}